# Mathematical Function
# Find maximum value in the list
print(max([10, 2, 30, 4, 55, 26, 28, 45]))

# Find minimum value in the list
print(min([10, 2, 30, 4, 55, 26, 28, 45]))

# Calculate sum of the list
print(sum([10, 2, 30, 4, 55, 26, 28, 45]))

# Round number to 2 decimal places
print(round(3.14162511, 2))

# Get length of string
print(len("Ebrahim"))

# Sort list in ascending order
sorted_list = sorted([10, 2, 30, 4, 55, 26, 28, 45])
print(sorted_list)

# Reverse sorted list
print(list(reversed(sorted_list)))

# Calculate average of the list
average = sum(sorted_list) / len(sorted_list)
print(average)