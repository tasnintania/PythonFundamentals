# Define a set with mixed data types
my_set = {1, 2, 3, "Python", "Java"}  # Contains integers and strings

# Add individual elements to the set
my_set.add("Javascript")  # Add "Javascript" to the set
my_set.add("Go")          # Add "Go" to the set
print(f"My set: {my_set}")  # Print the set after adding elements

# Update the set with multiple elements from a list
my_set.update([4, 5, 6])  # Add elements 4, 5, and 6 to the set
print(f"My updated set: {my_set}")  # Print the updated set