# Integer
number = 10

# Float
height = 5.9

# String
name = "Ebrahim"

# Boolean
is_active = True

# List
results = ["A", "A+", 1, 3, 3]

# Tuple
coordinates = (1, 2, 3)

# Dictionary
person = {
    "Name": "Tania",
    "University": "BUBT",
    "Batch": 43,
    "Email": "<EMAIL>"
}

# Set
colors = {"Red", "Green", "Blue", 2, 3, 4}

# Printing variables
print("Integer:", number)
print("Float:", height)
print("String:", name)
print("Boolean:", is_active)
print("List:", results)
print("Tuple:", coordinates)
print("Dictionary:", person)
print("Set:", colors)
print(f"Integer: {number}")

# Checking Variable Type
print("Type of number is:", type(number))
print(f"Type of {number} is {type(number)}")
print("Type of Dictionary is:", type(person))