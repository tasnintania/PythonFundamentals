# Define variables
number1 = 120
number2 = 45

# Addition
result1 = number1 + number2
print("Addition:", result1)  # Output: 165

# Subtraction
result2 = number1 - number2
print("Subtraction:", result2)  # Output: 75

# Multiplication
result3 = number1 * number2
print("Multiplication:", result3)  # Output: 5400

# Division
result4 = number1 / number2
print("Division:", result4)  # Output: 2.6666666666666665

# Exponentiation
num1 = 2
num2 = 3
result5 = num1 ** num2
print("Exponentiation:", result5)  # Output: 8