# Define variables
x = 10
y = 10
z = 30

# Equal
print("x == y:", x == y)  # Output: True
print("y == z:", y == z)  # Output: False

# Not Equal
print("y != z:", y != z)  # Output: True

# Greater Than
print("z > x:", z > x)  # Output: True

# Less Than
print("y < z:", y < z)  # Output: True

# Greater Than or Equal To
print("x >= y:", x >= y)  # Output: True

